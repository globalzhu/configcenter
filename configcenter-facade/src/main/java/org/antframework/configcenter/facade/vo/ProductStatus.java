package org.antframework.configcenter.facade.vo;

/**
 * 产品状态
 */
public enum ProductStatus {
    /**
     * 售前阶段
     */
    PRE_SALE,

    /**
     * 交付中
     */
    DELIVER
    ,

    /**
     * 售后维保
     */
    AFTER_SALE
    ,

    /**
     * 废弃
     */
    DELETED
    ,

    /**
     * 暂停
     */
    SUSPEND
    ,

    /**
     * 已完结
     */
    FINISHED

}
